create table thesis
(
    thesis_no       integer generated by default as identity (minvalue 100000 maxvalue 999999)
        primary key,
    title           varchar(500) not null,
    abstract        text         not null	unique,
    author_id       varchar(11)  not null
      references authors,
    type            varchar(50)  not null,
    uni_id          integer      not null
        references university,
    ins_id          integer      not null
        references institutes,
    num_pages       integer      not null,
    language        varchar(50)  not null,
    submission_date date         not null,
    sup_id          varchar(11)
        references supervisors,
    co_sup_id       varchar(11)
        references co_supervisors
);


create table authors
(
    author_id      varchar(11) not null
        constraint persons_pkey
            primary key,
    author_name    varchar(50),
    author_surname varchar(50),
    author_age     smallint
);


create table co_supervisors
(
    co_sup_id      varchar(11)  not null
        primary key,
    co_sup_name    varchar(100) not null,
    co_sup_surname varchar(100) not null,
    co_sup_age     smallint     not null
);


create table institutes
(
    ins_id   integer generated always as identity (maxvalue 99999)
        primary key,
    ins_name varchar(100) not null,
    uni_id   integer      not null
        references university
);

create table keywords
(
    key_id    integer     not null
        primary key,
    key_name  varchar(50) not null,
    thesis_no integer
        references thesis
);

create table subject_topics
(
    st_id   integer      not null
        primary key,
    st_name varchar(200) not null
);

create table supervisors
(
    sup_id      varchar(11) not null
        primary key,
    sup_name    varchar(100),
    sup_surname varchar(100),
    sup_age     smallint
);

create table t_subjects
(
    id        integer not null
        primary key,
    thesis_no integer
        references thesis,
    st_id     integer
        references subject_topics
);

create table university
(
    uni_id      integer generated always as identity (maxvalue 9999)
        primary key,
    uni_name    varchar(50) not null,
    uni_address varchar(60) not null
);




